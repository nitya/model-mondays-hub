#!/usr/bin/env node
// scripts/scrape-playlist.mjs
//
// Scrapes the PUBLIC Model Mondays YouTube playlist page (no API key
// required) to discover episode ids, titles, and metadata, and writes/updates
// episode YAML records under data/episodes/. Uses yt-dlp (a well-maintained,
// no-auth YouTube extractor) under the hood in --flat-playlist mode to list
// videos, then fetches per-video metadata (description, upload date, view
// counts, chapters) for each one.
//
// Usage:
//   node scripts/scrape-playlist.mjs [--limit N] [--playlist <url>]
//
// This is also invoked by the `update-episode` and `update-series` skills.
// Requires `yt-dlp` to be on PATH (pip install yt-dlp / brew install yt-dlp).
import { execFileSync } from "node:child_process";
import fs from "node:fs";
import path from "node:path";
import yaml from "js-yaml";
import { EPISODES_DIR } from "./lib/paths.mjs";
import { slugify } from "./lib/slug.mjs";

const DEFAULT_PLAYLIST =
  "https://www.youtube.com/playlist?list=PLmsFUfdnGr3wzz6a4E-Szksg92JPng-AL";

function parseArgs(argv) {
  const args = { limit: Infinity, playlist: DEFAULT_PLAYLIST };
  for (let i = 0; i < argv.length; i++) {
    if (argv[i] === "--limit") args.limit = Number(argv[++i]);
    if (argv[i] === "--playlist") args.playlist = argv[++i];
  }
  return args;
}

function ytdlpJson(args) {
  const out = execFileSync("yt-dlp", args, {
    maxBuffer: 1024 * 1024 * 64,
    encoding: "utf8",
  });
  return JSON.parse(out);
}

function parseChapters(description) {
  // Matches lines like "12:34 Segment Title" commonly used for YouTube chapters.
  const chapters = [];
  const re = /^(\d{1,2}:\d{2}(?::\d{2})?)\s+(.+)$/gm;
  let m;
  while ((m = re.exec(description || ""))) {
    chapters.push({ time: m[1], label: m[2].trim() });
  }
  return chapters;
}

function guessSegmentType(title, description) {
  const text = `${title} ${description}`.toLowerCase();
  const types = [];
  if (text.includes("tech spotlight") || text.includes("technology spotlight")) types.push("tech-spotlight");
  if (text.includes("partner spotlight")) types.push("partner-spotlight");
  if (text.includes("customer story") || text.includes("customer spotlight")) types.push("customer-story");
  if (text.includes("study corner")) types.push("study-corner");
  if (text.includes("season premiere")) types.push("season-premiere");
  if (text.includes("season finale") || text.includes("closing")) types.push("season-finale");
  if (text.includes("ask me anything") || text.includes(" ama")) types.push("ama");
  return types.length ? types : ["tech-spotlight"];
}

function writeEpisodeYaml(id, doc) {
  const file = path.join(EPISODES_DIR, `${id}.yaml`);
  const header = `# Auto-generated by scripts/scrape-playlist.mjs — review before merging.\n`;
  fs.writeFileSync(file, header + yaml.dump(doc, { lineWidth: 100 }));
  return file;
}

function main() {
  const args = parseArgs(process.argv.slice(2));
  console.log(`Fetching playlist entries from ${args.playlist} ...`);

  let flat;
  try {
    flat = ytdlpJson(["--flat-playlist", "-J", args.playlist]);
  } catch (err) {
    console.error("Failed to fetch playlist with yt-dlp:", err.message);
    console.error(
      "Is yt-dlp installed? Try: pip install yt-dlp  (or brew install yt-dlp)"
    );
    process.exit(1);
  }

  const entries = (flat.entries || []).slice(0, args.limit);
  console.log(`Found ${entries.length} playlist entrie(s) to process.`);

  const written = [];
  for (const entry of entries) {
    const videoId = entry.id;
    console.log(`Fetching metadata for ${videoId} ...`);
    let full;
    try {
      full = ytdlpJson(["-J", `https://www.youtube.com/watch?v=${videoId}`]);
    } catch (err) {
      console.warn(`  skipped ${videoId}: ${err.message.split("\n")[0]}`);
      continue;
    }

    const title = full.title || entry.title;
    const id = slugify(title);
    const uploadDate = full.upload_date
      ? `${full.upload_date.slice(0, 4)}-${full.upload_date.slice(4, 6)}-${full.upload_date.slice(6, 8)}`
      : undefined;

    const doc = {
      id,
      title,
      description: (full.description || "").split("\n\n")[0].trim(),
      season: "season-unknown",
      publishDate: uploadDate,
      status: "published",
      youtubeVideoId: videoId,
      youtubeUrl: `https://www.youtube.com/watch?v=${videoId}`,
      segmentType: guessSegmentType(title, full.description || ""),
      speakers: [],
      tags: [],
      duration: full.duration,
      metrics: {
        views: full.view_count,
        likes: full.like_count,
        comments: full.comment_count,
        updatedAt: new Date().toISOString(),
      },
      transcriptPath: `data/transcripts/${id}.md`,
      chapters: parseChapters(full.description),
      provenance: "scraped:youtube-playlist",
    };

    written.push(writeEpisodeYaml(id, doc));
  }

  console.log(`\nWrote ${written.length} episode file(s):`);
  for (const f of written) console.log(`  - ${f}`);
  console.log(
    "\nNOTE: review 'season', 'speakers', and 'tags' fields — this script " +
      "cannot reliably infer them and leaves safe defaults/empties for a human " +
      "or the update-episode skill to fill in."
  );
}

main();
